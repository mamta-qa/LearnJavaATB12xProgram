package ForLoop;

public class forloop {
    public static void main(String[] args) {
        {


            for (long i = 0 ; i < 10; i++)
                System.out.println(" value of i is " + i);
        }

        for(float f = 0.0f; f < 10.67; f++) {
            System.out.println("Hi,Float -> " + f);
        }
    }}